/**************************************************************************************************
* 功能：看门狗初始化
* 参数：无
* 描述：溢出时间0.629s
**************************************************************************************************/
void WDT_Init(void)
{
	RSTFLAG |= 0x04;   										//设置看门狗复位需要检测P3.2的状态，否则看门狗复位后进入USB下载模式
	WDT_CONTR = (D_EN_WDT + D_CLR_WDT + D_WDT_SCALE_32);	//使能看门狗，清零看门狗定时器，设置溢出时间0.629s
}

/**************************************************************************************************
* 功能：喂狗
* 参数：无
* 描述：无
**************************************************************************************************/
void Feed_Dog(void)
{
	WDT_CONTR = (D_EN_WDT + D_CLR_WDT + D_WDT_SCALE_32);	//使能看门狗，清零看门狗定时器，设置溢出时间0.629s
}